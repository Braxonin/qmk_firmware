static void render_led_status(void) {
    led_t led_state = host_keyboard_led_state();
    oled_set_cursor(0, 0);
    oled_write_P(led_state.caps_lock   ? PSTR("CAP") : PSTR("   "), false);
    oled_set_cursor(0, 1);
    oled_write_P(led_state.num_lock    ? PSTR("NUM") : PSTR("   "), false);
    oled_set_cursor(0, 2);
    oled_write_P(led_state.scroll_lock ? PSTR("SCR") : PSTR("   "), false);
}

static void format_bar(char *buf, uint8_t percent) {
    uint8_t filled = (percent > 100 ? 100 : percent) / 10;
    for (uint8_t i = 0; i < 10; i++) buf[i] = (i < filled) ? '=' : ' ';
    buf[10] = '\0';
}

static void render_system_monitor(void) {
    uint8_t cpu, gpu, ram;
    if (hid_fetch_stats(&cpu, &gpu, &ram)) {
        g_cpu = cpu; g_gpu = gpu; g_ram = ram;
        g_sysmon_last = timer_read32();
    }

    // redraw every second even if same data
    if (timer_elapsed32(g_sysmon_last) > 1000) {
        g_sysmon_last = timer_read32();
    }

    char bar[11], line[21];

    oled_set_cursor(0, 0);
    oled_write_P(PSTR("System Monitor"), false);

    format_bar(bar, g_cpu);
    snprintf(line, sizeof(line), "CPU:%3u%% %s", g_cpu, bar);
    oled_set_cursor(0, 1); oled_write(line, false);

    format_bar(bar, g_gpu);
    snprintf(line, sizeof(line), "GPU:%3u%% %s", g_gpu, bar);
    oled_set_cursor(0, 2); oled_write(line, false);

    format_bar(bar, g_ram);
    snprintf(line, sizeof(line), "RAM:%3u%% %s", g_ram, bar);
    oled_set_cursor(0, 3); oled_write(line, false);
}

