// Main OLED task
// -----------------------------------------------------------------------------
bool oled_task_user(void) {
    if (g_needs_clear) {
        oled_clear();
        g_needs_clear = false;
    }

    switch (g_mode) {
        case DISPLAY_MODE_LOGO:
            if (!g_logo_rendered) {
                oled_write_raw_P(Braxon, sizeof(Braxon));
                g_logo_timer   = timer_read32();
                g_logo_rendered = true;
            } else if (timer_elapsed32(g_logo_timer) > LOGO_DISPLAY_TIME) {
                g_mode = DISPLAY_MODE_LAYERS;
                g_needs_clear = true;
            }
            break;

        case DISPLAY_MODE_LAYERS: {
            uint8_t layer = get_highest_layer(layer_state);
            if (layer >= NUM_LAYERS) layer = 0;
            oled_write_raw_P(pop[layer], sizeof(pop[layer]));
        } break;

        case DISPLAY_MODE_SYSMON:
            render_system_monitor();
            break;

        case DISPLAY_MODE_CAT:
            if (timer_elapsed32(g_anim_timer) > ANIM_FRAME_DURATION) {
                g_anim_timer = timer_read32();
                g_idle_frame = (g_idle_frame + 1) % IDLE_FRAMES;
                oled_write_raw_P(idle[g_idle_frame], ANIM_SIZE);
            }
            render_led_status();
            break;
    }

    return false;
}

// -----------------------------------------------------------------------------
// Public helper — call from a keycode to cycle screens
// -----------------------------------------------------------------------------
void oled_display_mode_step(void) {
    g_mode = (g_mode + 1) % DISPLAY_MODE_COUNT;
    g_needs_clear = true;
    if (g_mode == DISPLAY_MODE_SYSMON) g_sysmon_refresh = true;
}
