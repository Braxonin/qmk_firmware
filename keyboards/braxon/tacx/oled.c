// -----------------------------------------------------------------------------
// HID callback (host → keyboard)
// -----------------------------------------------------------------------------
// HID callback (host → keyboard)
// VIA Custom Value callback (host → keyboard)
// This runs when VIA receives a custom command (0x07) with data.
#ifdef VIA_ENABLE
void via_custom_value_command_kb(uint8_t *data, uint8_t length) {
    // Check if the packet is long enough. We need at least 4 bytes:
    // data[0] = Command (0x07)
    // data[1] = CPU
    // data[2] = GPU
    // data[3] = RAM
    if (length < 4) return;

    // Read the values from the CORRECT indices in the array.
    uint8_t incoming_cpu = data[1]; // CPU is the first byte AFTER the command
    uint8_t incoming_gpu = data[2]; // GPU is the second byte
    uint8_t incoming_ram = data[3]; // RAM is the third byte

    // Check if any of the stats have changed to avoid unnecessary redraws.
    if (s_stats.cpu != incoming_cpu || s_stats.gpu != incoming_gpu || s_stats.ram != incoming_ram) {
        s_stats.cpu = incoming_cpu;
        s_stats.gpu = incoming_gpu;
        s_stats.ram = incoming_ram;
        g_sysmon_refresh = true;
    }
}
#endif





// Helper: fetch HID stats if updated
static bool hid_fetch_stats(uint8_t *cpu, uint8_t *gpu, uint8_t *ram) {
    if (!g_sysmon_refresh) return false;
    if (cpu) *cpu = s_stats.cpu;
    if (gpu) *gpu = s_stats.gpu;
    if (ram) *ram = s_stats.ram;
    g_sysmon_refresh = false;
    return true;
}

